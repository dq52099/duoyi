/// 设计思路：
/// - 当前项目 200+ 处中文硬编码；一次性全替换不现实。
/// - 引入 `AppLocale.zh` / `AppLocale.en` + `tr(key)` 函数式 API。
/// - 高频公共词条先入表，调用方按需迁移；未命中的 key 直接返回中文回退。
/// - `LocaleProvider` 持久化用户偏好（默认 `zh`）。
///
/// 添加新词条：在 `_zh` 与 `_en` 中同步追加同一 key。
library;

import 'dart:ui';

import 'package:flutter/foundation.dart';
import 'package:shared_preferences/shared_preferences.dart';

enum AppLocale { zh, en }

extension AppLocaleX on AppLocale {
  Locale get flutterLocale => switch (this) {
    AppLocale.zh => const Locale('zh', 'CN'),
    AppLocale.en => const Locale('en'),
  };

  String get storageValue => switch (this) {
    AppLocale.zh => 'zh',
    AppLocale.en => 'en',
  };

  static AppLocale fromLanguageCode(String? code) {
    return code?.toLowerCase() == 'en' ? AppLocale.en : AppLocale.zh;
  }

  static AppLocale? fromStorageValue(String? raw) {
    return switch (raw) {
      'zh' => AppLocale.zh,
      'en' => AppLocale.en,
      _ => null,
    };
  }
}

