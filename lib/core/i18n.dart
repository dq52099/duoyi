class I18n {
  I18n._();

  static AppLocale _current = AppLocale.zh;

  static AppLocale get current => _current;

  static void setLocale(AppLocale locale) {
    _current = locale;
  }

  /// 翻译键。如果当前 locale 字典缺失，先回退到中文，再回退到 key 本身。
  static String tr(String key) {
    final dict = _current == AppLocale.en ? _en : _zh;
    return dict[key] ?? _zh[key] ?? key;
  }
}

/// 持久化的 locale 偏好。
class LocaleProvider extends ChangeNotifier {
  static const _key = 'duoyi_locale_v1';

  AppLocale _locale = AppLocale.zh;

  AppLocale get locale => _locale;

  Locale get flutterLocale => _locale.flutterLocale;

  static AppLocale defaultLocaleForPlatform() {
    return AppLocaleX.fromLanguageCode(
      PlatformDispatcher.instance.locale.languageCode,
    );
  }

  Future<void> loadFromStorage() async {
    final prefs = await SharedPreferences.getInstance();
    _locale =
        AppLocaleX.fromStorageValue(prefs.getString(_key)) ??
        defaultLocaleForPlatform();
    I18n.setLocale(_locale);
  }

  Future<void> setLocale(AppLocale locale) async {
    if (_locale == locale) return;
    _locale = locale;
    I18n.setLocale(locale);
    final prefs = await SharedPreferences.getInstance();
    await prefs.setString(_key, locale.storageValue);
    notifyListeners();
  }
}
